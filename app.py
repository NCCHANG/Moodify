@app.route('/search', methods=['POST'])
def search():
    sp = get_spotify_client()
    if not sp:
        return jsonify({'error': 'not logged in'}), 401

    data = request.get_json()
    user_query = data.get('query', '')

    if not user_query:
        return jsonify({'error': 'no query provided'}), 400

    # Get taste profile (from session if already fetched, otherwise fetch now)
    taste_profile = session.get('taste_profile')
    if not taste_profile:
        taste_profile = build_taste_profile(sp)
        session['taste_profile'] = taste_profile

    # Step 1: LLM extracts search parameters from user's query
    search_params = extract_search_params(user_query)
    print('Search params:', search_params)  # helpful for debugging

    # Step 2: Last.fm builds candidate pool using those params
    candidates = build_candidate_pool(
        genres=search_params.get('genres', []),
        ref_artists=search_params.get('ref_artists', [])
    )
    print(f'Candidates found: {len(candidates)}')

    # Step 3: LLM ranks candidates against user taste + explains picks
    recommendations = rank_and_explain(user_query, taste_profile, candidates)

    return jsonify({
        'query': user_query,
        'search_params': search_params,  # useful to show user what the LLM understood
        'recommendations': recommendations
    })

@app.route('/save-playlist', methods=['POST'])
def save_playlist():
    sp = get_spotify_client()
    if not sp:
        return jsonify({'error': 'not logged in'}), 401

    data = request.get_json()
    query = data.get('query', 'Moodify playlist')
    songs = data.get('songs', [])

    # Create playlist
    try:
        playlist = sp.current_user_playlist_create(
            name=f"Moodify: {query}",
            public=False,
            description=f"Generated by Moodify for: {query}"
        )
    except Exception as e:
        print(f'Error creating playlist: {e}')
        return jsonify({'error': f'Failed to create playlist: {str(e)}'}), 500

    # Search each song on Spotify to get its URI
    track_uris = []
    for song in songs:
        try:
            result = sp.search(
                q=f"track:{song['name']} artist:{song['artist']}",
                type='track',
                limit=1
            )
            tracks = result['tracks']['items']
            if tracks:
                track_uris.append(tracks[0]['uri'])
        except Exception as e:
            print(f'Error searching for song {song["name"]}: {e}')

    # Add tracks to playlist
    try:
        if track_uris:
            sp.playlist_add_items(playlist['id'], track_uris)
    except Exception as e:
        print(f'Error adding tracks to playlist: {e}')
        return jsonify({'error': f'Failed to add tracks: {str(e)}'}), 500

    return jsonify({
        'success': True,
        'playlist_url': playlist['external_urls']['spotify'],
        'tracks_added': len(track_uris)
    })

@app.route('/recently-played')
def recently_played():
    recent_play_display_limit = 15
    sp = get_spotify_client()
    if not sp:
        return jsonify({'error': 'not logged in'}), 401

    recent = sp.current_user_recently_played(limit=recent_play_display_limit)
    tracks = []
    seen = set()

    for item in recent['items']:
        track = item['track']
        key = track['id']
        if key in seen:
            continue
        seen.add(key)
        tracks.append({
            'name': track['name'],
            'artist': track['artists'][0]['name'],
            'album': track['album']['name'],
            'image': track['album']['images'][1]['url'] if len(track['album']['images']) > 1 else track['album']['images'][0]['url'],
            'spotify_url': track['external_urls']['spotify']
        })

    return jsonify(tracks)
